GUIScrollPanel::GUIScrollPanel(GUIManager *Manager) : GUIPanel(Manager) {
	m_Skin = nullptr;
	m_DrawBitmap[0] = m_DrawBitmap[1] = m_DrawBitmap[2] = nullptr;
	m_ButtonSize = 17;
	m_MinimumKnobSize = 9;
	m_SmallChange = 1;
	m_ButtonPushed[0] = m_ButtonPushed[1] = false;
	m_GrabbedKnob = false;
	m_GrabbedBackg = false;
	m_RebuildSize = true;
	m_RebuildKnob = true;
	m_ValueResolution = 1;
}

