/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void GUIScrollPanel::Destroy() {
	// Destroy the three bitmaps
	for (int i = 0; i < 3; i++) {
		if (m_DrawBitmap[i]) {
			m_DrawBitmap[i]->Destroy();
			delete m_DrawBitmap[i];
			m_DrawBitmap[i] = 0;
		}
	}
}

